package com.atrule.ramadannotifier.classes;

public class Ramadan {
    String date, ramadan, sehriTime, aftarTime;

    public Ramadan() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getRamadan() {
        return ramadan;
    }

    public void setRamadan(String ramadan) {
        this.ramadan = ramadan;
    }

    public String getSehriTime() {
        return sehriTime;
    }

    public void setSehriTime(String sehriTime) {
        this.sehriTime = sehriTime;
    }

    public String getAftarTime() {
        return aftarTime;
    }

    public void setAftarTime(String aftarTime) {
        this.aftarTime = aftarTime;
    }
}
